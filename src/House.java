public class House {
    private String house;
    private String ui;

    public House() {
    }

    public House(String house, String ui) {
        this.house = house;
        this.ui = ui;
    }

    public String getHouse() {
        return house;
    }

    public void setHouse(String house) {
        this.house = house;
    }

    public String getUi() {
        return ui;
    }

    public void setUi(String ui) {
        this.ui = ui;
    }
}
