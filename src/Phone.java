public class Phone {
    private String aty;
    private String tusu;

    public Phone() {
    }

    public Phone(String aty, String tusu) {
        this.aty = aty;
        this.tusu = tusu;
    }

    public String getAty() {
        return aty;
    }

    public void setAty(String aty) {
        this.aty = aty;
    }

    public String getTusu() {
        return tusu;
    }

    public void setTusu(String tusu) {
        this.tusu = tusu;
    }
}
